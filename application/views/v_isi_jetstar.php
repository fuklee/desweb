<h3 id="isi_composition">Composition</h3>
<ul>
    <li>Website ini memiliki symmetrical balance dimana konten terbagi menjadi bagian - bagian yang simetris</li>
    <li>Website ini memiliki whitespace, konten digrupkan dalam 1 kategori yang sama, serta kontennya mudah dilihat dan dibaca (proximity)</li>
    <li>Emphasis - Placement, pada website ini peletekan pusat ada pada titik tengah halaman yang mengarahkan pengunjung pada bagian promo dan pemesanannya sama dengan website airasia.com.</li>
    <li>Emphasis - Continuance, website ini memberikan tampilan yang bertahap dari atas hingga bawah. Dari bagian header yang berisi logo kemudian turun kebagian body yang berisi konten promo utama kemudian mengarah kepada bagian pemesanan langsung, dilanjutkan ke tampilan footer dan menu - menu lainnya.</li>
    <li>Emphasis - Contrast, pada halaman utama terdapat bagian promo tiket penerbangan yang berisi gambar - gambar menarik namun warnanya kurang kontras karena bagian keseluruhan halaman sudah menggunakan warna yang juga kontras.</li>
    <li>Emphasis - Proportion, pada bagian promo dibuat lebih besar dari pada bagian yang lainnya sehingga mengarahkan pengunjung kepada promo tersebut.</li>
    <li>Yang ditekankan pada konten web ini adalah pemesanan tiket secara custom dan website ini mengikuti tren expansive footer navigation </li>
