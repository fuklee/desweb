<h3 id="isi_composition">Composition</h3>
<ul>
    <li>Website ini memiliki symmetrical balance dimana konten terbagi menjadi bagian - bagian yang simetris</li>
    <li>Website ini memiliki whitespace, konten digrupkan dalam 1 kategori yang sama, serta kontennya mudah dilihat dan dibaca (proximity)</li>
    <li>Emphasis - Placement, pada website ini peletekan pusat ada pada titik tengah halaman yang mengarahkan pengunjung pada bagian pemesanannya langsung.</li>
    <li>Emphasis - Continuance, website ini memberikan tampilan yang bertahap dari atas hingga bawah. Dari bagian header yang berisi logo kemudian turun kebagian body yang berisi bagian pemesanan, dilanjutkan ke daftar penawaran produk.</li>
    <li>Emphasis - Contrast, pada halaman utama terdapat gambar yang besar dibawah menu pemesanan yang lebih kecil sehingga pengunjung dapat langsung melihat website pada bagian pemesanan secara langsung ketika pertama kali dibuka.</li>
    <li>Emphasis - Proportion, pada bagian pemesanan (background bagian pemesanan) dibuat lebih besar dari pada bagian yang lainnya sehingga mengarahkan pengunjung kepada bagian pemesanan tersebut.</li>
    <li>Konten website ini juga terdapat repetition berdasarkan grup kontennya, yakni tiket murah dan tentang tiger air (repetition)</li>
    <li>Yang ditekankan pada konten web ini adalah produk tiket murahnya dan website ini mengikuti tren expansive footer navigation </li>
</ul>

