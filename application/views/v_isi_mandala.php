<h3 id="isi_typo">Typography</h3>
<p>
    Ada dua jenis font yang digunakan di website Tiger Air ini yaitu , Sans-serif dan Cursive. Font Sans-Serif adalah font yang tidak memiliki dekorasi pada ujungnya. Sedangkan Font Cursive adalah jenis font yang seperti tulisan manusia,  biasanya font ini digunakan untuk memperindah bagian tertentu dari dokumen. 
